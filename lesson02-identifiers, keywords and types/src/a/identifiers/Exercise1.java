package a.identifiers;

public class Exercise1 {

	public static void main(String[] args) {
		// define new variable for each primely type and print it.
		
		//Round numbers:
		byte b = 12;
		short s = 156;
		int i = 7121;
		long l = 1615181315;
		
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		
		System.out.println();
		
		
		//Decimal numbers:
		float f = 5.5F;
		double d = 1.268419;
		
		System.out.println(f);
		System.out.println(d);
		
		System.out.println();
		
		//Boolean:
		boolean bo = true;
		
		System.out.println(bo);
		
		System.out.println();
		
		//Characters:
		char c = '!';
		
		System.out.println(c);
	}

}
